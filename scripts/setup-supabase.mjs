#!/usr/bin/env node
/**
 * Interactive Supabase + env setup.
 * Run: node scripts/setup-supabase.mjs
 *
 * Get keys from: Supabase Dashboard → Project Settings → API
 */

import { createInterface } from "readline";
import { randomBytes } from "crypto";
import { writeFileSync } from "fs";
import { resolve, dirname } from "path";
import { fileURLToPath } from "url";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const envPath = resolve(root, ".env.local");

const rl = createInterface({ input: process.stdin, output: process.stdout });

function ask(question) {
  return new Promise((resolve) => rl.question(question, resolve));
}

function trim(s) {
  return s.trim();
}

async function main() {
  console.log("\nPhotoshoot Booking — Supabase setup\n");
  console.log("Open: https://supabase.com/dashboard → your project → Settings → API\n");

  const url = trim(
    await ask("Project URL (https://xxxxx.supabase.co): ")
  );
  const anon = trim(await ask("anon public key: "));
  const service = trim(await ask("service_role secret key: "));

  let adminPassword = trim(
    await ask("Admin password for /admin (Enter = auto-generate): ")
  );
  if (!adminPassword) {
    adminPassword = randomBytes(16).toString("hex");
    console.log(`Generated ADMIN_PASSWORD: ${adminPassword}`);
    console.log("(Save this — you need it to block days on /admin)\n");
  }

  const appUrl =
    trim(await ask("App URL [http://localhost:3000]: ")) ||
    "http://localhost:3000";

  if (!url.includes("supabase.co") || !anon.startsWith("eyJ") || !service.startsWith("eyJ")) {
    console.error("\nValues look incomplete. Check Project Settings → API in Supabase.");
    rl.close();
    process.exit(1);
  }

  const content = `# Generated by scripts/setup-supabase.mjs
NEXT_PUBLIC_SUPABASE_URL=${url}
NEXT_PUBLIC_SUPABASE_ANON_KEY=${anon}
SUPABASE_SERVICE_ROLE_KEY=${service}

ADMIN_PASSWORD=${adminPassword}
NEXT_PUBLIC_APP_URL=${appUrl}
`;

  writeFileSync(envPath, content, "utf8");
  console.log(`\nWrote ${envPath}`);
  console.log("\nNext steps:");
  console.log("  1. Supabase → SQL Editor → run supabase/schema.sql");
  console.log("  2. npm run dev");
  console.log("  3. Open http://localhost:3000/api/health — should show ok: true\n");

  rl.close();
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
